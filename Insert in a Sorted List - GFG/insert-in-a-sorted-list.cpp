class Solution{
  public:
    // Should return head of the modified linked list
    Node *sortedInsert(struct Node* head, int data) {
        // Code here
        if(head->data>data)
        {
            Node* tmp=new Node(data);
            tmp->next=head;
            return tmp;
        }
            
        Node* cur=head;
        int fg=0;
        Node* prev=NULL;
        while(cur!=NULL)
        {
            if(cur->data>data)
            {   
                 Node* tmp=new Node(data);
                prev->next=tmp;
                tmp->next=cur;
                fg=1;
                break;
            }
            prev=cur;
            cur=cur->next;
        }
        
        if(fg==0)
        {
            prev->next=new Node(data);
        
            
        }
        
        return head;
    }
};


