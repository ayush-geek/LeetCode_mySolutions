/*
  Pairwise swap a linked list
  The input list will have at least one element
  node is defined as

struct Node
{
    int data;
    struct Node* next;

    Node(int x){
        data = x;
        next = NULL;
    }

}*head;
*/
class Solution
{
    public:
    Node* pairWiseSwap(struct Node* head) 
    {
        // The task is to complete this method
         Node* tmp=NULL;
         Node* prev=NULL;
        while(head!=NULL && head->next!=NULL)
        {
            // swap(head->data,head->next->data);
            Node* t1=head->next;
            Node* t2=head->next->next;
            Node* cur=head;
            if(prev!=NULL)
                prev->next=t1;
            else
                tmp=t1;
            t1->next=cur;
            cur->next=t2;
            head=t2;
            prev=cur;
        }
        if(tmp==NULL)
            return head;
        return tmp;
    }
};

